palette_generator = ColorPaletteGenerator()

def generate_site(title, description, style):

    colors = palette_generator.get_palette(style)
    html = build_html(title, description, colors)

    return html, html

import gradio as gr

with gr.Blocks(css=custom_css, theme=gr.themes.Base()) as demo:

    gr.Markdown("# 🚀 HTML Quick-Styler Advanced")
    gr.Markdown("### Professional HTML Page Generator with Auto-Styling")

    with gr.Tabs():

        # TAB 1: Generate Page
        with gr.Tab("Generate Page"):

            with gr.Row():
                with gr.Column():
                    title = gr.Textbox(label="Page Title")
                    desc = gr.Textbox(label="Page Description & Sections")
                    style = gr.Dropdown(
                        ["modern","vibrant","ocean","forest",
                         "sunset","luxury","creative","minimal"],
                        label="Color Style"
                    )
                    btn = gr.Button("🔥 Generate HTML")

                with gr.Column():
                    code_output = gr.Code(label="Generated HTML", language="html")

        # TAB 2: Live Preview
        with gr.Tab("Live Preview"):
            preview = gr.HTML()
            load_btn = gr.Button("Load Preview")

        # TAB 3: Color Palettes
        with gr.Tab("Color Palettes"):
            palette_style = gr.Radio(
                ["modern","vibrant","ocean","forest",
                 "sunset","luxury","creative","minimal"],
                label="Choose Style"
            )
            palette_output = gr.Textbox(label="Palette Colors")

        # TAB 4: Templates
        with gr.Tab("Templates"):
            gr.Markdown("### Quick Templates")
            gr.Markdown("""
            • Portfolio Website
            • SaaS Landing Page
            • Company Website
            """)

        # TAB 5: Guide
        with gr.Tab("Guide"):
            gr.Markdown("""
            ## How It Works

            1. Enter Title
            2. Enter Description
            3. Select Color Style
            4. Click Generate
            5. Preview your page
            """)

    # Button Functions
    btn.click(generate_site, inputs=[title, desc, style],
              outputs=[preview, code_output])

    load_btn.click(generate_site, inputs=[title, desc, style],
                   outputs=[preview, code_output])

    palette_style.change(
        lambda x: palette_generator.get_palette(x),
        inputs=palette_style,
        outputs=palette_output
    )

demo.launch(share=True)