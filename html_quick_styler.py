class ColorPaletteGenerator:

    def __init__(self):
        self.palettes = {
            "modern": ["#6366f1", "#4f46e5", "#0f172a", "#f8fafc"],
            "vibrant": ["#ec4899", "#db2777", "#111827", "#f3f4f6"],
            "ocean": ["#0ea5e9", "#0369a1", "#0c4a6e", "#e0f2fe"],
            "forest": ["#16a34a", "#166534", "#052e16", "#dcfce7"],
            "sunset": ["#f97316", "#ea580c", "#7c2d12", "#fff7ed"],
            "luxury": ["#d4af37", "#b8860b", "#111111", "#f5f5dc"],
            "creative": ["#a855f7", "#7e22ce", "#1e1b4b", "#ede9fe"],
            "minimal": ["#374151", "#1f2937", "#ffffff", "#111827"]
        }

    def get_palette(self, style):
        return self.palettes.get(style, self.palettes["modern"])

